--DROP TABLE Attendee;
CREATE TABLE Attendee 
(
	  attendee_ID INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	  first_name  VARCHAR(50) NOT NULL,
	  last_name VARCHAR(50) NOT NULL,
	  email VARCHAR(50) NOT NULL,
	  phone VARCHAR(25) NULL
);
COMMENT ON TABLE Attendee IS 'Table to store information about attendees';

--DROP TABLE Event_Tickets; 
CREATE TABLE Event_Tickets 
(
	ticket_ID uuid PRIMARY KEY DEFAULT uuid_generate_v4() NOT NULL,
	event_ID INT NOT NULL,
	ticket_type VARCHAR(50),
	price MONEY,
	FOREIGN KEY (event_ID) REFERENCES Event(event_ID)
);
COMMENT ON TABLE Event_Tickets IS 'Table to store information about event tickets';
